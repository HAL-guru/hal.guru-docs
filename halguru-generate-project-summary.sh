#!/bin/bash
#
# Calculate lines of code for the project.
# This script runs 'cloc' on the parent directory,
# excluding non-source files such as JSON, XML, HTML, CSS, etc.
#

clear_manual_files() {
  echo "Removing autogenerated manual files"

  find "hal.guru-docs" -type f -name "autogen-*.md" -delete
}

generate_manual_files() {
  echo "Generating manual files for $version"

  cd "hal.guru-docs"

  append_to_summary "## Manual files"
  append_to_summary ""
  append_to_summary "Documentation for core version $version"
  append_to_summary ""

  ./halguru-generate-manual.sh > /dev/null

  append_to_summary "| File Type | Files | Lines | Per File |"
  append_to_summary "|----------|------:|------:|----------:|"

  local total_files_count=$(count_files "docs" "*.md")
  local total_lines_count=$(count_lines "docs" "*.md")
  local total_average_count=$(( total_lines_count / total_files_count ))
  local autogen_files_count=$(count_files "docs" "autogen-*.md")
  local autogen_lines_count=$(count_lines "docs" "autogen-*.md")
  local autogen_average_count=$(( autogen_lines_count / autogen_files_count ))
  local markdown_files_count=$(( total_files_count - autogen_files_count ))
  local markdown_lines_count=$(( total_lines_count - $autogen_lines_count ))
  local markdown_average_count=$(( markdown_lines_count / markdown_files_count ))

  append_to_summary "| Markdown | $markdown_files_count | $markdown_lines_count | $markdown_average_count |"
  append_to_summary "| Autogenerated | $autogen_files_count | $autogen_lines_count | $autogen_average_count |"
  append_to_summary "| Total | $total_files_count | $total_lines_count | $total_average_count |"
  append_to_summary ""
  append_to_summary "File Types:"
  append_to_summary ""
  append_to_summary "* **Markdown** - Markdown files (\`*.md\`) available in the [public repository](https://github.com/HAL-guru/hal.guru-docs)."
  append_to_summary "* **Autogenerated** - Markdown files (\`autogen-*.md\`) generated by the [halguru CLI](https://docs.hal.guru/installation)."
  append_to_summary "* **Total** - All Markdown files."
  append_to_summary ""
  append_to_summary "Columns:"
  append_to_summary ""
  append_to_summary "* **Files** - Total number of Markdown files of that type."
  append_to_summary "* **Lines** - Total number of lines across all Markdown files of that type."
  append_to_summary "* **Per File** - Average number of lines per Markdown file."
  append_to_summary ""
  append_to_summary "Directories and subdirectories excluded from the calculations: \`$code_exclude_directories\`"
  append_to_summary ""

  cd ..
}

build_find_name_expr() {
  local input="$1"
  local -a items
  local i
  local -a result

  IFS=',' read -r -a items <<< "$input"

  for i in "${!items[@]}"; do
    [[ -z "${items[$i]}" ]] && continue
    if [ "${#result[@]}" -gt 0 ]; then
      result+=( -o )
    fi
    result+=( -name "${items[$i]}" )
  done

  printf "%q " "${result[@]}"
}

count_files() {
  local search_dir="${1:-.}"
  local file_patterns="${2:-$code_files_pattern}"
  local exclude_directories="${3:-$code_exclude_directories}"

  local -a find_expr
  local -a prune_expr

  eval "find_expr=($(build_find_name_expr "$file_patterns"))"
  eval "prune_expr=($(build_find_name_expr "$exclude_directories"))"

  local -a find_cmd=(find "$search_dir")

  if [ "${#prune_expr[@]}" -gt 0 ]; then
    find_cmd+=( \( "${prune_expr[@]}" \) -type d -prune -o )
  fi

  find_cmd+=( -type f )

  if [ "${#find_expr[@]}" -gt 0 ]; then
    find_cmd+=( \( "${find_expr[@]}" \) )
  fi

  find_cmd+=( -print )

  "${find_cmd[@]}" | wc -l
}

count_lines() {
  local search_dir="${1:-.}"
  local file_patterns="${2:-$code_files_pattern}"
  local exclude_directories="${3:-$code_exclude_directories}"

  local -a find_expr
  local -a prune_expr

  eval "find_expr=($(build_find_name_expr "$file_patterns"))"
  eval "prune_expr=($(build_find_name_expr "$exclude_directories"))"

  local -a find_cmd=(find "$search_dir")

  if [ "${#prune_expr[@]}" -gt 0 ]; then
    find_cmd+=( \( "${prune_expr[@]}" \) -type d -prune -o )
  fi

  find_cmd+=( -type f )

  if [ "${#find_expr[@]}" -gt 0 ]; then
    find_cmd+=( \( "${find_expr[@]}" \) )
  fi

  find_cmd+=( -print0 )

  "${find_cmd[@]}" | xargs -0 wc -l | grep -v " total$" | awk '{sum += $1} END {print sum + 0}'
}

append_to_summary() {
  echo "$@" >> "$summary_file"
}

generate_git_information() {
  echo "Generating Git information for repositories"

  append_to_summary "## Git Information"
  append_to_summary ""

  append_to_summary "| Repository | Commits | Files  | Lines | Created | Updated |"
  append_to_summary "|------------|--------:|-------:|------:|---------|---------|"

  total_commits_count=0
  total_files_count=0
  total_lines_count=0

  generate_git_table_row "hal.guru-robots-core"
  generate_git_table_row "hal.guru-apps"
  generate_git_table_row "hal.guru-licensing"
  generate_git_table_row "hal.guru-maui"
  generate_git_table_row "hal.guru-docs"
  generate_git_table_row "hal.guru-website"

  append_to_summary "| **Total** | $total_commits_count | $total_files_count  | $total_lines_count |  |  |"

  append_to_summary ""
  append_to_summary "Columns:"
  append_to_summary ""
  append_to_summary "* **Repository** - The name of the Git repository."
  append_to_summary "* **Commits** - The total number of commits in the repository."
  append_to_summary "* **Files** - The total number of files in the repository."
  append_to_summary "* **Lines** - The total number of lines across all files in the repository."
  append_to_summary "* **Created** - The date when the repository was created."
  append_to_summary "* **Updated** - The date of the most recent change in the repository."
  append_to_summary ""
  append_to_summary "Files included in the calculations: \`$code_files_pattern\`"
  append_to_summary ""
  append_to_summary "Directories and subdirectories excluded from the calculations: \`$code_exclude_directories\`"
  append_to_summary ""
  append_to_summary "All lines are counted, including comments and empty lines."
  append_to_summary "Binary files, graphics, icons, and PDF files are **not** counted."
  append_to_summary ""
}

generate_git_table_row() {
  local repo_name="$1"
  local file_patterns="${2:-$code_files_pattern}"
  local exclude_directories="${3:-$code_exclude_directories}"
  echo "- '$repo_name'"
  cd "$repo_name" || return
  local commits_count=$(git log --format="%H" | wc -l)
  local created_date=$(git log --reverse --format=%ad --date=format:'%Y-%m-%d' | head -n 1)
  local updated_date=$(git log -1 --format=%ad --date=format:'%Y-%m-%d')
  local files_count=$(count_files)
  local lines_count=$(count_lines)
  local name="${repo_name:9}"

  append_to_summary "| $name | $commits_count | $files_count | $lines_count | $created_date | $updated_date |"

  total_commits_count=$(( total_commits_count + commits_count ))
  total_files_count=$(( total_files_count + files_count ))
  total_lines_count=$(( total_lines_count + lines_count ))
  cd ..
}

generate_content_information() {
  echo "Generating cdn.hal.guru information for repositories"

  cd "hal.guru-content"

  append_to_summary "## cdn.hal.guru"
  append_to_summary ""
  append_to_summary "The repository stores all fonts, graphic assets, and templates,"
  append_to_summary "that are available to other applications via [cdn.hal.guru](https://cdn.hal.guru)."
  append_to_summary ""

  append_to_summary "| Images  | Fonts | JS Files | JS Lines | CSS Files | CSS Lines |"
  append_to_summary "|--------:|------:|---------:|---------:|----------:|----------:|"

  local images_count=$(count_files . "*.png,*.jpg,*.svg,*.ico,*.gif" ".git,.idea")
  local fonts_count=$(count_files . "*.ttf,*.woff2" ".git,.idea")
  local js_files_count=$(count_files . "*.js" ".git,.idea")
  local js_lines_count=$(count_lines . "*.js" ".git,.idea")
  local css_files_count=$(count_files . "*.css,*.less,*.scss" ".git,.idea")
  local css_lines_count=$(count_lines . "*.css,*.less,*.scss" ".git,.idea")

  append_to_summary "| $images_count | $fonts_count | $js_files_count | $js_lines_count | $css_files_count | $css_lines_count |"

  local commits_count=$(git log --format="%H" | wc -l)
  local created_date=$(git log --reverse --format=%ad --date=format:'%Y-%m-%d' | head -n 1)
  local updated_date=$(git log -1 --format=%ad --date=format:'%Y-%m-%d')

  append_to_summary ""
  append_to_summary "* Commits: $commits_count"
  append_to_summary "* Created: $created_date"
  append_to_summary "* Updated: $updated_date"
  append_to_summary ""

  cd ..
}

function generate_front_matter() {
  append_to_summary "---"
  append_to_summary "title: Project Summary"
  append_to_summary "description: Current status of the hal.guru project - quantitative metrics like lines of code, commits, and overall progress."
  append_to_summary "autogenerated: true"
  append_to_summary "core_version: $version"
  append_to_summary "last_update: $current_date"
  append_to_summary "---"
  append_to_summary ""
}

function generate_header_information() {
  append_to_summary "Current status of the hal.guru project for $current_date."
  append_to_summary "Quantitative metrics like lines of code, commits, and overall progress."
  append_to_summary ""
}

function generate_footer_information() {
  append_to_summary "## Technical Information"
  append_to_summary ""
  append_to_summary "These tables help estimate the size and structure of the project,"
  append_to_summary "not its complexity or quality, so the results should be treated as a quantitative overview"
  append_to_summary "rather than a measure of development effort."
  append_to_summary ""
  append_to_summary "This file is generated by [halguru_generate-project-summary.sh](https://github.com/HAL-guru/hal.guru-docs/blob/main/halguru-generate-project-summary.sh) tool."
}

function generate_cloc_summary() {
  echo "Genrating lines of code"

  append_to_summary "## Lines of code summary"
  append_to_summary ""
  append_to_summary "Results of running the \`cloc\` tool on the following repositories: core, apps, licensing, maui, docs, and website."
  append_to_summary ""
  include_dirs="hal.guru-robots-core hal.guru-apps hal.guru-licensing hal.guru-maui hal.guru-docs hal.guru-website"
  cloc $include_dirs --md --exclude-dir="$code_exclude_directories" | sed '1,2d; $d' >> "$summary_file"
  append_to_summary ""
  append_to_summary "The table shows a summary of the codebase by language or file type."
  append_to_summary ""
  append_to_summary "* **Language** – the detected programming or markup language."
  append_to_summary "* **files** – the number of files recognized as that language."
  append_to_summary "* **blank** – empty lines."
  append_to_summary "* **comment** – lines containing comments."
  append_to_summary "* **code** – lines containing actual source code."
  append_to_summary ""
  append_to_summary "Directories and subdirectories excluded from the calculations: \`$code_exclude_directories\`"
  append_to_summary ""
}

echo "Generating 'project-summary.md' file"

script_dir="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

echo "Script directory: $script_dir"

trap 'cd "$script_dir";' EXIT

code_files_pattern="*.cs,*.razor,*.md,*.sh,*.ps1,*.py,*.yml,*.yaml"
code_exclude_directories=".git,.idea,site,public,resources,__pycache__,bin,obj"
current_date="$(date +%F)"
summary_file="$script_dir/docs/status/project-summary.md"
copyright=$(halguru --version)
version=$(awk '{print $2}' <<< "$copyright")

cd "$script_dir" || exit 1

: > "$summary_file"

cd ..

current_dir="$(pwd)"
echo "Repositories directory: $current_dir"

generate_front_matter
generate_header_information
clear_manual_files
generate_git_information
generate_manual_files
generate_content_information
generate_cloc_summary
generate_footer_information

echo "Done. Check file '$summary_file'"
